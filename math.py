friends = 5

friends += 1 #Augumented Assignment Operator.
#Or you could: friends = friends + 1
friends -= 3
friends *= 3
friends /= 3 #Becomes a Float variable
friends **= 2
remainder = friends % 2 #Useful to check if even or odd!
print(f"friends: {friends}")
print(f"remainder: {remainder}")


